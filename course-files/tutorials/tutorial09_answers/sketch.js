function drawRow (row, topX, topY, pixelWidth, palette=['transparent', 'hotpink', 'black', 'white'], outlineColor='#CCC') {
    for (let i = 0; i < row.length; i++) {
        const color = palette[row[i]];
        stroke(outlineColor);
        fill(color);
        square(topX, topY, pixelWidth);
        topX += pixelWidth;
    }
}


function drawPixelArt(grid, topX, topY, pixelWidth, palette=['transparent', 'hotpink', 'black', 'white'], outlineColor='#CCC') {
    for (let i = 0; i < grid.length; i++) {
        // draw each row at the specified (x, y) position:
        drawRow(grid[i], topX, topY, pixelWidth, palette, outlineColor);
        topY += pixelWidth;
    }
}